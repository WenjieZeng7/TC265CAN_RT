#include "IfxPort.h"
#include "Bsp.h"
#include "GPIO_RTC.h"

#define WAIT_TIME 2
#define WAIT_TIME_10ms 10

/*********************************************************************************************************************/
/*-----------------------------------------------------Macros--------------------------------------------------------*/
/*********************************************************************************************************************/
#define SCLK    &MODULE_P14, 10 /* Port pin for the sclk  */
#define SERIAL  &MODULE_P14, 8  /* Port pin for the io  */
#define CE      &MODULE_P14, 6  /* Port pin for the ce  */

int clock_data[8];
int temp_byte[8];
/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/



/* Function to configure the port pins for the push button and the LED */
void init_GPIOs(void)
{
    IfxPort_setPinMode(SCLK, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(CE, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(SERIAL, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinState(SCLK, IfxPort_State_low);
    IfxPort_setPinState(CE, IfxPort_State_low);
    IfxPort_setPinState(SERIAL, IfxPort_State_low);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
}

void Write_Byte(int content)
{
    IfxPort_setPinMode(SERIAL, IfxPort_Mode_outputPushPullGeneral);
    for (int i=0;i<8;i++){
             int x = (content & (1 << i));  //Write to Address
             Send_Bit(x);
             SCLK_Pulse_LH();
        }
}

void Read_Byte(int addr)
{
    int temp = 0;
    Write_Byte(addr);
    IfxPort_setPinMode(SERIAL, IfxPort_Mode_inputNoPullDevice);
    for (int j = 0; j < 8; j++)
    {
        SCLK_Pulse_HL();
        temp >>= 1;
        if ((IfxPort_getPinState(SERIAL)) == 0x01)
        {
            temp |= 128;
        }
        else
        {
            temp &= 127;
        }
    }
}

void Read_Burst(void)
{
    CE_On();
    Write_Byte(ADR_RD_BURST);
    IfxPort_setPinMode(SERIAL, IfxPort_Mode_inputNoPullDevice); //set as input for receiving data
    int temp;
    for (int i = 0; i < 8; i++)
    {
        temp = 0;
        for (int j = 0; j < 8; j++)
        {
            SCLK_Pulse_HL();
            temp >>= 1;
            if ((IfxPort_getPinState(SERIAL)) == 0x01)
            {
                temp |= 128;
            }
            else
            {
                temp &= 127;
            }
        }
        clock_data[i] = temp;
    }
    CE_Off();
}

void SCLK_Pulse_HL(void)
{
    IfxPort_setPinState(SCLK, IfxPort_State_high);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
    IfxPort_setPinState(SCLK, IfxPort_State_low);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
}

void SCLK_Pulse_LH(void)
{
    IfxPort_setPinState(SCLK, IfxPort_State_low);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
    IfxPort_setPinState(SCLK, IfxPort_State_high);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
}

void Send_Bit (unsigned int value)
{
    if (value != 0){
        IfxPort_setPinState(SERIAL, IfxPort_State_high);}
    else {
        IfxPort_setPinState(SERIAL, IfxPort_State_low);
    }
}

void CE_On (void)
{
    IfxPort_setPinState(CE, IfxPort_State_high);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
}

void CE_Off (void)
{
    IfxPort_setPinState(CE, IfxPort_State_low);
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME));
}

void settime(void)
{
    CE_On();
    Write_Byte(ADR_WR_CREG);  //goto control register
    Write_Byte(0x00);         //disable write protect
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_TCR);  //Go to Trickle charge register
    Write_Byte(171);  //Enable Charger, 2 diodes, 8kOhm
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_SEC);  //Go to seconds register
    Write_Byte(0x00);  //set as..
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_MIN);  //Go to minute register
    Write_Byte(0x05);  //set as..
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_HR);  //Go to hour register
    Write_Byte(0x08);  //set as..
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_DATE);  //Go to date register
    Write_Byte(0x20);  //set as..
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_MON);  //Go to month register
    Write_Byte(0x09);  //set as..
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_DAY);  //Go to day register
    Write_Byte(0x01);  //set as..
    CE_Off();
    CE_On();
    Write_Byte(ADR_WR_YR);  //Go to year register
    Write_Byte(0x21);  //set as..21
    CE_Off();
    CE_On();
    Read_Burst();
    CE_Off();
}
